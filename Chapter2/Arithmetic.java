package Chapter2;

public class Arithmetic {

    public int Square(int num) {
        return num * num;
    }

    public int sumSquare(int num1, int num2) {
        return num1 * num1 + num2 * num2;
    }

    public int differSquare(int num1, int num2) {
        return num1 * num1 - num2 * num2;
    }
}
